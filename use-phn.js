// while loop

var usePhone = ["Nokia1100", "Iteal", "Sampony", "Samsang", "Relme"];
var i = 0;
while (i < usePhone.length) {
    var element = usePhone[i];
    i++;
    console.log(element);
}

// for loop

var usePhone = ["Nokia1100", "Iteal", "Sampony", "Samsang", "Relme"];
for (var i = 0; i < usePhone.length; i++) {
    var element = usePhone[i];
    console.log(element);
}